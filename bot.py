user_favorites = {}

def load_recipes():
    try:
        response = requests.get(RECIPE_URL)
        response.raise_for_status()
        return response.json()
    except requests.RequestException as e:
        logging.error(f"–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ —Ä–µ—Ü–µ–ø—Ç–æ–≤: {e}")
        return []
    except ValueError as e:
        logging.error(f"–û—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ JSON: {e}")
        return []

def get_categories():
    categories = set(recipe.get('category') for recipe in recipes)
    return sorted(categories)

def format_recipe(recipe, servings=1):
    recipe_text = f"üçΩ **{recipe['title']}**\n\n"
    recipe_text += "üìù **–ò–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç—ã:**\n"
    for ingredient in recipe.get('ingredients', []):
        amount = ingredient.get('amount', '')
        recipe_text += f"üî∏ {ingredient['ingredient']:20} {amount * servings}\n"
    recipe_text += "\nüßë‚Äçüç≥ **–ü—Ä–∏–≥–æ—Ç–æ–≤–ª–µ–Ω–∏–µ:**\n"
    for i, step in enumerate(recipe.get('instructions', []), start=1):
        recipe_text += f"{i}. {step}\n"
    return recipe_text

def categorize_recipe(recipe_title):
    """–ü–æ–∏—Å–∫ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ —Ä–µ—Ü–µ–ø—Ç–∞ –ø–æ –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º"""
    for category, keywords in CATEGORIES.items():
        pattern = r'\b(?:' + '|'.join(re.escape(kw) for kw in keywords) + r')\b'
        if re.search(pattern, recipe_title, re.IGNORECASE):
            return category
    return "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ"

# –ö–æ–º–∞–Ω–¥–∞ —Å—Ç–∞—Ä—Ç
async def start(update: Update, context: CallbackContext):
    global recipes
    recipes = load_recipes()
    if not recipes:
        await update.message.reply_text("–ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–≥—Ä—É–∑–∏—Ç—å —Ä–µ—Ü–µ–ø—Ç—ã. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ.")
        return

    categories = get_categories()
    keyboard = [[InlineKeyboardButton(f"{CATEGORY_EMOJIS.get(category, 'üç¥')} {category}", callback_data=f'category_{category}_0')] for category in categories]
    reply_markup = InlineKeyboardMarkup(keyboard)
    await update.message.reply_text('–í—ã–±–µ—Ä–∏—Ç–µ –∫–∞—Ç–µ–≥–æ—Ä–∏—é —Ä–µ—Ü–µ–ø—Ç–æ–≤:', reply_markup=reply_markup)

# –ü–æ–∏—Å–∫ —Ä–µ—Ü–µ–ø—Ç–æ–≤ –ø–æ –∏–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç–∞–º
async def search_recipes(update: Update, context: CallbackContext):
    query = update.message.text.split(' ', 1)
    if len(query) < 2:
        await update.message.reply_text("–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, —É–∫–∞–∂–∏—Ç–µ –∏–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç—ã –¥–ª—è –ø–æ–∏—Å–∫–∞.")
        return

    ingredient = query[1].lower()
    matching_recipes = [recipe for recipe in recipes if ingredient in [ing['ingredient'].lower() for ing in recipe.get('ingredients', [])]]

    if not matching_recipes:
        await update.message.reply_text("–†–µ—Ü–µ–ø—Ç—ã –Ω–µ –Ω–∞–π–¥–µ–Ω—ã.")
        return

    await update.message.reply_text("–ù–∞–π–¥–µ–Ω—ã —Ä–µ—Ü–µ–ø—Ç—ã:")
    for recipe in matching_recipes:
        await update.message.reply_text(recipe['title'])

# –°–ª—É—á–∞–π–Ω—ã–π —Ä–µ—Ü–µ–ø—Ç
async def random_recipe(update: Update, context: CallbackContext):
    if not recipes:
        await update.message.reply_text("–†–µ—Ü–µ–ø—Ç—ã –Ω–µ –∑–∞–≥—Ä—É–∂–µ–Ω—ã.")
        return
    recipe = random.choice(recipes)
    recipe_text = format_recipe(recipe)
    await update.message.reply_text(recipe_text, parse_mode='Markdown')

# –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –≤ –∏–∑–±—Ä–∞–Ω–Ω–æ–µ
async def add_to_favorites(update: Update, context: CallbackContext):
    user_id = update.message.from_user.id
    recipe_index = int(context.args[0])
    if user_id not in user_favorites:
        user_favorites[user_id] = []
    user_favorites[user_id].append(recipe_index)
    await update.message.reply_text("–†–µ—Ü–µ–ø—Ç –¥–æ–±–∞–≤–ª–µ–Ω –≤ –∏–∑–±—Ä–∞–Ω–Ω–æ–µ.")

# –ü–æ–∫–∞–∑ –∏–∑–±—Ä–∞–Ω–Ω—ã—Ö —Ä–µ—Ü–µ–ø—Ç–æ–≤
async def show_favorites(update: Update, context: CallbackContext):
    user_id = update.message.from_user.id
    if user_id not in user_favorites or not user_favorites[user_id]:
        await update.message.reply_text("–ò–∑–±—Ä–∞–Ω–Ω—ã–µ —Ä–µ—Ü–µ–ø—Ç—ã –ø—É—Å—Ç—ã.")
        return
    for index in user_favorites[user_id]:
        await update.message.reply_text(recipes[index]['title'])

# –ö–∞—Å—Ç–æ–º–∏–∑–∞—Ü–∏—è –ø–æ—Ä—Ü–∏–π
async def customize_servings(update: Update, context: CallbackContext):
    recipe_index = int(context.args[0])  # –ò–Ω–¥–µ–∫—Å —Ä–µ—Ü–µ–ø—Ç–∞
    servings = int(context.args[1]) if len(context.args) > 1 else 1
    recipe = recipes[recipe_index]
    recipe_text = format_recipe(recipe, servings)
    await update.message.reply_text(recipe_text, parse_mode='Markdown')

# –≠–∫—Å–ø–æ—Ä—Ç —Ä–µ—Ü–µ–ø—Ç–∞ –≤ PDF
async def export_recipe(update: Update, context: CallbackContext):
    recipe_index = int(context.args[0])
    recipe = recipes[recipe_index]
    pdf_file = f"{recipe['title']}.pdf"
    c = canvas.Canvas(pdf_file, pagesize=letter)
    c.drawString(100, 750, f"–†–µ—Ü–µ–ø—Ç: {recipe['title']}")
    # –î–æ–±–∞–≤—å—Ç–µ –æ—Å—Ç–∞–ª—å–Ω—ã–µ –¥–µ—Ç–∞–ª–∏ —Ä–µ—Ü–µ–ø—Ç–∞
    c.save()
    await update.message.reply_document(open(pdf_file, 'rb'))

# –ö–∞—Ç–µ–≥–æ—Ä–∏—è
async def category_button(update: Update, context: CallbackContext):
    query = update.callback_query
    await query.answer()
    data = query.data.split('_')
    category = data[1]
    page = int(data[2])

    recipes_in_category = [recipe for recipe in recipes if categorize_recipe(recipe['title']) == category]
    start_index = page * BUTTONS_PER_PAGE
    end_index = start_index + BUTTONS_PER_PAGE
    buttons = []
    for i, recipe in enumerate(recipes_in_category[start_index:end_index], start=start_index):
        buttons.append([InlineKeyboardButton(recipe['title'], callback_data=f"recipe_{i}")])

    # –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫–∏ –Ω–∞–≤–∏–≥–∞—Ü–∏–∏
    nav_buttons = []
    if start_index > 0:
        nav_buttons.append(InlineKeyboardButton("–ù–∞–∑–∞–¥", callback_data=f"category_{category}_{page-1}"))
    if end_index < len(recipes_in_category):
        nav_buttons.append(InlineKeyboardButton("–í–ø–µ—Ä–µ–¥", callback_data=f"category_{category}_{page+1}"))
    if nav_buttons:
        buttons.append(nav_buttons)

    reply_markup = InlineKeyboardMarkup(buttons)
    await query.edit_message_text(f"–†–µ—Ü–µ–ø—Ç—ã –≤ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏: {category}", reply_markup=reply_markup)

